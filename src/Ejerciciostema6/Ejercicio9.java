package Ejerciciostema6;

public class Ejercicio9 { //Escribir todos los números del 0 al 100 de 9 en 9. No mostrar números fuera del rango [0, 100]

    public static void main(String[] args) {
                      
        System.out.println("Escribir todos los números del 0 al 100 de 9 en 9");
        
        for (int a = 0; a <= 100; a += 9)
        {

            System.out.println(a);

        }
        System.out.println("Cuenta finalizada");

    }
    
}